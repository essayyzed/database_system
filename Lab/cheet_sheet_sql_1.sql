SELECT PARK_CODE, TICKET_NO,
TICKET_TYPE, TICKET_PRICE, 
TICKET_PRICE + ROUND((TICKET_PRICE*0.1),2) AS 'PRICE INCREASED' FROM TICKET;




/***********GREATER THAN**************/
SELECT PARK_CODE, TICKET_TYPE, TICKET_PRICE
FROM TICKET
WHERE TICKET_PRICE > 20;

/***********LESS THAN**************/
SELECT PARK_CODE, TICKET_TYPE, TICKET_PRICE
FROM TICKET
WHERE TICKET_PRICE < 30;

/******************CHARACTER COMPARISONS***********************/
SELECT PARK_CODE, PARK_NAME, PARK_COUNTRY
FROM THEMEPARK
