/*******************LOGICAL Operators****************************************/
/*********************AND*************************/
SELECT EMP_NUM, ATTRACT_NO
FROM HOURS
WHERE HOURS_PER_ATTRACT > 3
AND DATE_WORKED > '18-MAY-2007';

SELECT * 
FROM ATTRACTION
WHERE ATTRACT_AGE <= 10 AND ATTRACT_CAPACITY < 100 AND ATTRACT_NAME IS NOT NULL;

/**************OR-OPERATOR*********************************/

SELECT PARK_NAME, PARK_COUNTRY
FROM THEMEPARK
WHERE PARK_COUNTRY = "FR" 
OR PARK_COUNTRY = "UK";

SELECT * 
FROM ATTRACTION
WHERE (PARK_CODE LIKE 'FR%'
AND ATTRACT_CAPACITY < 50) 
OR (ATTRACT_CAPACITY > 100);

/************NOT_OPERATOR********************/
